xs = [3, 1, 2, 0, 4, 9]
print(xs[2])
print(xs[-2])

# xs[2] retorna o valor que está na posição 2 (terceiro elemento): 2
# xs[-2] acessa o índice -2 (penúltima posição) e retorna o valor 4
# Exemplo: índices começam em 0; xs[0] = 3, xs[1] = 1, xs[2] = 2
# Índices negativos contam de trás para frente: xs[-1] é o último, xs[-2] é o penúltimo
